/*
 * Единственная атомарная операция создания гостя + бронирования.
 * Цена и снимок услуг передаются только доверенным Express-сервером;
 * anon/authenticated не имеют EXECUTE.
 */
CREATE OR REPLACE FUNCTION create_booking_atomic(
  p_cabin_id UUID,
  p_check_in DATE,
  p_check_out DATE,
  p_guest_name TEXT,
  p_guest_phone TEXT,
  p_guest_telegram TEXT,
  p_comment TEXT,
  p_guests_count INTEGER,
  p_total_price INTEGER,
  p_extras_snapshot JSONB DEFAULT '[]'::jsonb
)
RETURNS SETOF bookings
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_guest_id UUID;
  v_booking_id UUID;
BEGIN
  IF p_check_out <= p_check_in THEN
    RAISE EXCEPTION 'Дата выезда должна быть позже даты заезда';
  END IF;
  IF p_guests_count < 1 OR p_total_price < 0 THEN
    RAISE EXCEPTION 'Некорректные параметры бронирования';
  END IF;

  PERFORM pg_advisory_xact_lock(hashtext(p_cabin_id::text));
  IF EXISTS (
    SELECT 1 FROM external_bookings eb
    WHERE eb.cabin_id = p_cabin_id
      AND daterange(eb.check_in, eb.check_out, '[)') && daterange(p_check_in, p_check_out, '[)')
  ) THEN
    RAISE EXCEPTION 'Даты уже заняты во внешнем календаре';
  END IF;

  SELECT g.id INTO v_guest_id
  FROM guests AS g
  WHERE g.phone = p_guest_phone
  ORDER BY g.created_at ASC
  LIMIT 1
  FOR UPDATE;

  IF v_guest_id IS NULL THEN
    INSERT INTO guests (full_name, phone, telegram)
    VALUES (p_guest_name, p_guest_phone, NULLIF(p_guest_telegram, ''))
    RETURNING id INTO v_guest_id;
  ELSE
    UPDATE guests
    SET full_name = p_guest_name,
        telegram = NULLIF(p_guest_telegram, '')
    WHERE id = v_guest_id;
  END IF;

  INSERT INTO bookings (
    cabin_id, guest_id, check_in, check_out, guests_count,
    status, total_price, comment, extras_snapshot
  ) VALUES (
    p_cabin_id, v_guest_id, p_check_in, p_check_out, p_guests_count,
    'pending', p_total_price, NULLIF(p_comment, ''), COALESCE(p_extras_snapshot, '[]'::jsonb)
  ) RETURNING id INTO v_booking_id;

  RETURN QUERY SELECT b.* FROM bookings AS b WHERE b.id = v_booking_id;
END;
$$;

REVOKE ALL ON FUNCTION create_booking_atomic(UUID, DATE, DATE, TEXT, TEXT, TEXT, TEXT, INTEGER, INTEGER, JSONB)
  FROM PUBLIC, anon, authenticated;
GRANT EXECUTE ON FUNCTION create_booking_atomic(UUID, DATE, DATE, TEXT, TEXT, TEXT, TEXT, INTEGER, INTEGER, JSONB)
  TO service_role;

/* Полная замена событий одного источника выполняется одной транзакцией. */
CREATE OR REPLACE FUNCTION replace_external_bookings(
  p_source_id UUID,
  p_events JSONB
)
RETURNS INTEGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_source external_calendar_sources%ROWTYPE;
  v_count INTEGER := 0;
BEGIN
  SELECT * INTO v_source
  FROM external_calendar_sources
  WHERE id = p_source_id
  FOR UPDATE;

  IF NOT FOUND THEN RAISE EXCEPTION 'Источник календаря не найден'; END IF;

  PERFORM pg_advisory_xact_lock(hashtext(v_source.cabin_id::text));

  DELETE FROM external_bookings WHERE source_id = p_source_id;

  INSERT INTO external_bookings (
    source_id, cabin_id, external_uid, source_name, summary,
    check_in, check_out, raw_event, last_seen_at
  )
  SELECT
    v_source.id,
    v_source.cabin_id,
    event->>'uid',
    v_source.source_name,
    NULLIF(event->>'summary', ''),
    (event->>'check_in')::date,
    (event->>'check_out')::date,
    COALESCE(event->'raw_event', '{}'::jsonb),
    now()
  FROM jsonb_array_elements(COALESCE(p_events, '[]'::jsonb)) AS event
  WHERE event ? 'uid' AND event ? 'check_in' AND event ? 'check_out';

  GET DIAGNOSTICS v_count = ROW_COUNT;

  UPDATE external_calendar_sources
  SET last_synced_at = now(), last_sync_status = 'success', last_sync_error = NULL
  WHERE id = p_source_id;

  RETURN v_count;
END;
$$;

REVOKE ALL ON FUNCTION replace_external_bookings(UUID, JSONB)
  FROM PUBLIC, anon, authenticated;
GRANT EXECUTE ON FUNCTION replace_external_bookings(UUID, JSONB) TO service_role;

