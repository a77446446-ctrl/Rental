/* Атомарное сохранение списка iCal-источников одного домика. */
CREATE OR REPLACE FUNCTION save_external_calendar_sources(
  p_cabin_id UUID,
  p_sources JSONB
)
RETURNS SETOF external_calendar_sources
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  PERFORM pg_advisory_xact_lock(hashtext(p_cabin_id::text));

  DELETE FROM external_calendar_sources ecs
  WHERE ecs.cabin_id = p_cabin_id
    AND NOT EXISTS (
      SELECT 1
      FROM jsonb_array_elements(COALESCE(p_sources, '[]'::jsonb)) source
      WHERE source->>'id' = ecs.id::text
    );

  INSERT INTO external_calendar_sources (id, cabin_id, source_name, ical_url, is_active)
  SELECT
    COALESCE(NULLIF(source->>'id', '')::uuid, gen_random_uuid()),
    p_cabin_id,
    left(COALESCE(NULLIF(source->>'source_name', ''), 'Внешний источник'), 80),
    source->>'ical_url',
    COALESCE((source->>'is_active')::boolean, true)
  FROM jsonb_array_elements(COALESCE(p_sources, '[]'::jsonb)) source
  WHERE COALESCE(source->>'ical_url', '') <> ''
  ON CONFLICT (id) DO UPDATE SET
    source_name = EXCLUDED.source_name,
    ical_url = EXCLUDED.ical_url,
    is_active = EXCLUDED.is_active;

  RETURN QUERY
    SELECT * FROM external_calendar_sources ecs
    WHERE ecs.cabin_id = p_cabin_id
    ORDER BY ecs.created_at;
END;
$$;

REVOKE ALL ON FUNCTION save_external_calendar_sources(UUID, JSONB)
  FROM PUBLIC, anon, authenticated;
GRANT EXECUTE ON FUNCTION save_external_calendar_sources(UUID, JSONB) TO service_role;

/* Домик, его фото, наполнение, теги и iCal-источники сохраняются одной транзакцией. */
CREATE OR REPLACE FUNCTION save_cabin_full(
  p_cabin_id UUID,
  p_name TEXT,
  p_description TEXT,
  p_base_price INTEGER,
  p_capacity INTEGER,
  p_is_active BOOLEAN,
  p_images JSONB,
  p_amenities JSONB,
  p_tags JSONB,
  p_sources JSONB
)
RETURNS JSONB
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_id UUID := COALESCE(p_cabin_id, gen_random_uuid());
  v_row cabins%ROWTYPE;
BEGIN
  IF length(trim(COALESCE(p_name, ''))) = 0 THEN RAISE EXCEPTION 'Название обязательно'; END IF;
  IF p_base_price < 0 OR p_capacity < 1 THEN RAISE EXCEPTION 'Некорректные цена или вместимость'; END IF;
  PERFORM pg_advisory_xact_lock(hashtext(v_id::text));

  IF EXISTS (SELECT 1 FROM cabins WHERE id = v_id) THEN
    UPDATE cabins SET
      name = left(trim(p_name), 255),
      description = COALESCE(p_description, ''),
      base_price = p_base_price,
      capacity = p_capacity,
      is_active = p_is_active,
      images_urls = ARRAY(SELECT image #>> '{}' FROM jsonb_array_elements(COALESCE(p_images, '[]'::jsonb)) image)
    WHERE id = v_id RETURNING * INTO v_row;
  ELSE
    INSERT INTO cabins (id, slug, name, description, base_price, capacity, is_active, images_urls)
    VALUES (
      v_id,
      'house-' || left(replace(v_id::text, '-', ''), 12),
      left(trim(p_name), 255),
      COALESCE(p_description, ''),
      p_base_price,
      p_capacity,
      p_is_active,
      ARRAY(SELECT image #>> '{}' FROM jsonb_array_elements(COALESCE(p_images, '[]'::jsonb)) image)
    ) RETURNING * INTO v_row;
  END IF;

  INSERT INTO app_config (key, value) VALUES ('amenities', jsonb_build_object(v_id::text, COALESCE(p_amenities, '[]'::jsonb)))
  ON CONFLICT (key) DO UPDATE SET value = jsonb_set(app_config.value, ARRAY[v_id::text], COALESCE(p_amenities, '[]'::jsonb), true);

  INSERT INTO app_config (key, value) VALUES ('cabin_tags', jsonb_build_object(v_id::text, COALESCE(p_tags, '[]'::jsonb)))
  ON CONFLICT (key) DO UPDATE SET value = jsonb_set(app_config.value, ARRAY[v_id::text], COALESCE(p_tags, '[]'::jsonb), true);

  PERFORM save_external_calendar_sources(v_id, COALESCE(p_sources, '[]'::jsonb));
  RETURN to_jsonb(v_row);
END;
$$;

REVOKE ALL ON FUNCTION save_cabin_full(UUID, TEXT, TEXT, INTEGER, INTEGER, BOOLEAN, JSONB, JSONB, JSONB, JSONB)
  FROM PUBLIC, anon, authenticated;
GRANT EXECUTE ON FUNCTION save_cabin_full(UUID, TEXT, TEXT, INTEGER, INTEGER, BOOLEAN, JSONB, JSONB, JSONB, JSONB)
  TO service_role;
